Select S.IDTPSERVICO, A.IDLOJA, A.IDATENDIMENTO, A.DTATEND, A.HHINI, A.HHFIM
, C.NOME, TS.DSCSERVICO, TT.DSCTRATAMENTO, TA.DSCAREA
From OATENDIMENTO A 
JOIN OCLIENTE C On A.IDCLIENTE=C.IDCLIENTE
Join OSESSAO S On A.IDLOJA=S.IDLOJA AND A.IDATENDIMENTO=S.IDATENDIMENTO
Join OTPSERVICO TS On S.IDTPSERVICO=TS.IDTPSERVICO
Join OTPTRATAMENTO TT On S.IDTPSERVICO=TT.IDTPTRATAMENTO
Join OAREA TA On S.IDAREA=TA.IDAREA
Join OEVENTOAGENDA E On A.IDLOJA=E.IDLOJA AND A.IDEVENTO=E.IDEVENTO AND FLGCANCELADO=0
Left Join OATENDIMENTO_VENDA AV On AV.IDLOJA=A.IDLOJA AND AV.IDATENDIMENTO=A.IDATENDIMENTO
Where AV.IDVENDA Is Null
and S.IDTPSERVICO=1
Order By DTATEND